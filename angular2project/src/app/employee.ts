export class Employee {

    FirstName:string;
    LastName:string;
    Salary:number;
    Departament:string;


    constructor(FirstName:string,LastName:string,Salary:number,Departament:string){
        this.FirstName=FirstName;
        this.LastName=LastName;
        this.Salary=Salary;
        this.Departament=Departament
    }
}
